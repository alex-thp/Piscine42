#include <unistd.h>

int		main(int ac, char **av)
{
	int		j;
	char	k;

	j = 0;
	if (ac == 2)
	{
		while (av[1][j] != '\0')
		{
			if (av[1][j] >= 'a' && av[1][j] <= 'z')
			{
				k = 'z' - (av[1][j] - 'a');
				write(1, &k, 1);
			}
			else if (av[1][j] >= 'A' && av[1][j] <= 'Z')
			{
				k =  'Z' - (av[1][j] - 'A');
				write(1, &k, 1);
			}
			else
				write(1, &(av[1][j]), 1);
			j++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
